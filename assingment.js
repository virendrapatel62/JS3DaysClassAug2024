// Temperature Conversion:
// Write a function fahrenheitToCelsius
// to convert Fahrenheit to Celsius.

function fahrenheitToCelsius(f) {
  const cel = ((f - 32) * 5) / 9;
  return cel;
}

console.log(fahrenheitToCelsius(92));

// Square a Number:
// Implement a function square that returns the
// square of a given number.

function squareOfANumber(number) {
  return number * number;
}

console.log(squareOfANumber(2));
